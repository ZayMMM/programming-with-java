package com.mm.bee.tech;

public class RelationalOperators {
	// less than <
	// greater than >
	// equal to ==
	// not equal to !=
	// less than equal to =<
	// greater than equal to >=
	
	public static void main(String[] args) {
		
		int x = 6;
		int y = 7;
		
		boolean result = x == y;
		
		double a = 8.8;
		double b = 9.8;
		
		boolean doubleResult = a > b;
		
		System.out.println("Result : " + doubleResult);
	}

}
